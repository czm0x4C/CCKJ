void DCMI_Start(void)
{  
  //	LCD_SetCursor(0,0);  
	//LCD_WriteRAM_Prepare();					//开始写入GRAM
	__HAL_DMA_ENABLE(&hdma_dcmi); 	//使能DMA
	DCMI->CR|=DCMI_CR_CAPTURE;    			//DCMI捕获使能 
}

//DCMI,关闭传输
void DCMI_Stop(void)
{
	DCMI->CR&=~(DCMI_CR_CAPTURE);  			//关闭捕获  
	while(DCMI->CR&0X01);					//等待传输结束 
	__HAL_DMA_DISABLE(&hdma_dcmi);	//关闭DMA 	
} 

void jpeg_data_process(void)
{
	if(jpeg_data_ok==0)	//jpeg数据还未采集完?
	{
		__HAL_DMA_DISABLE(&hdma_dcmi);//关闭DMA
		while(DMA2_Stream1->CR&0X01);	//等待DMA2_Stream1可配置   
		jpeg_data_len=PictureSize-__HAL_DMA_GET_COUNTER(&hdma_dcmi);//得到剩余数据长度
		jpeg_data_ok=1; 				//标记JPEG数据采集完按成,等待其他函数处理
	}
	if(jpeg_data_ok==2)	//上一次的jpeg数据已经被处理了
	{
		__HAL_DMA_SET_COUNTER(&hdma_dcmi,PictureSize);//传输长度为jpeg_buf_size*4字节	
		__HAL_DMA_ENABLE(&hdma_dcmi); //打开DMA
		jpeg_data_ok=0;						//标记数据未采集
	}
} 

void HAL_DCMI_FrameEventCallback(DCMI_HandleTypeDef *hdcmi)
{
	__HAL_DCMI_CLEAR_FLAG(hdcmi,DCMI_FLAG_FRAMERI);//清除帧中断

		jpeg_data_process();//jpeg数据处理
    //重新使能帧中断,因为HAL_DCMI_IRQHandler()函数会关闭帧中断
    __HAL_DCMI_ENABLE_IT(hdcmi,DCMI_IT_FRAME);
}
